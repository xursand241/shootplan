let currentLang = localStorage.getItem('shootplan_lang') || 'uz';

function t(key) {
    return translations[currentLang][key] || key;
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            // Check if it's an input with placeholder
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.hasAttribute('placeholder')) {
                    el.setAttribute('placeholder', translations[currentLang][key]);
                }
            } else {
                el.innerHTML = translations[currentLang][key];
            }
        }
    });
    
    // Maxsus til teksti uchun
    const langText = document.getElementById('currentLangText');
    if(langText) {
        if(currentLang === 'uz') langText.innerHTML = `Til: O'zbek`;
        if(currentLang === 'ru') langText.innerHTML = `Язык: Русский`;
        if(currentLang === 'en') langText.innerHTML = `Language: English`;
    }
}

function changeLanguageSettings(lang) {
    currentLang = lang;
    localStorage.setItem('shootplan_lang', lang);
    applyTranslations();
    
    // Agar sahifa render qilinadigan bo'lsa (index.html kabi), ularni qayta chaqirish kerak:
    if (typeof renderShoots === 'function') renderShoots();
    if (typeof renderCalendar === 'function') renderCalendar();
    if (typeof renderClients === 'function') renderClients();
    if (typeof renderProfile === 'function') renderProfile();
}

window.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
});
